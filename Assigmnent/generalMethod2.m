% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
equation = get(handles.edit8, 'String');
equation =  strrep(equation,'e^','exp');
initial = get(handles.edit9, 'String');
numOfiteration = get(handles.edit10, 'String');
if(numOfiteration == "")
    numOfiteration = "50";
end
epslion = get(handles.edit11, 'String');
if(epslion == "")
    epslion = "0.00001";
end
funcHandle = str2func(['@(x)' equation]);
x = linspace(str2double(initial)-2,str2double(initial)+2,100);
yfun = funcHandle(x);
plot(handles.axes,x,yfun,'LineWidth',2);
hold on;
cal = General(equation,str2double(initial));        
% Get object from the method to solve
[y,w,i] = cal.solve(handles,equation,str2double(epslion),round(str2double(numOfiteration)));
set(handles.edit12,'string',sprintf('%f',y));
set(handles.edit13,'string',sprintf('%f',w));
set(handles.edit14,'string',sprintf('%d',i));



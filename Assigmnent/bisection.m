% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
equation = get(handles.edit1, 'String');
equation =  strrep(equation,'e^','exp');
interval = get(handles.edit2, 'String');
first = extractBetween(get(handles.edit2, 'String'),1," ");
second = extractAfter(interval," ");
numOfiteration = get(handles.edit3, 'String');
if(numOfiteration == "")
    numOfiteration = "50";
end
epslion = get(handles.edit4, 'String');
if(epslion == "")
    epslion = "0.00001";
end
cla(handles.axes,'reset');
 funcHandle = str2func(['@(x)' equation]);
 x = linspace(str2double(first)-2,str2double(second)+2,100);
 y = funcHandle(x);
 plot(handles.axes,x,y,'LineWidth',2);
 hold on;
cal = ClassBisection(equation,str2double(first),str2double(second));        
[y,w,i,t]=cal.solve(handles,equation,str2double(epslion),round(str2double(numOfiteration)),"Bisection");
set(handles.edit5,'string',sprintf('%f',y));
set(handles.edit6,'string',sprintf('%f',w));
set(handles.edit7,'string',sprintf('%d',i));
set(handles.edit8,'string',sprintf('%f',t));



